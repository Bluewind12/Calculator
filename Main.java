public class Main {
    int Plus(int alpha, int beta) {
        System.out.println(alpha + " + " + beta + " = " + (alpha + beta));
        return alpha + beta;
    }

    int Minus(int alpha, int beta) {
        System.out.println(alpha + " - " + beta + " = " + (alpha - beta));
        return alpha - beta;
    }

    int Multiply(int alpha, int beta) {
        System.out.println(alpha + " * " + beta + " = " + (alpha * beta));
        return alpha * beta;
    }

    int Division(int alpha, int beta) {
        System.out.println(alpha + " / " + beta + " = " + (alpha / beta));
        return alpha / beta;
    }

}